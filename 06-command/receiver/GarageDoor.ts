export class GarageDoor {
  up(): void {
    console.log('Garage door is open');
  }

  down(): void {
    console.log('Garage door is closed');
  }

  stop(): void {
    console.log('Garage door is stopped');
  }

  lightOn(): void {
    console.log('Garage light is on');
  }

  lightOff(): void {
    console.log('Garage light is off');
  }
}
